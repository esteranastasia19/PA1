                        Kualitas air <br>
                        Menurut Balai Riset Perikanan Air Tawar, Pusat Riset Perikanan Budidaya, kualitas air yang diinginkan ikan batak adalah sebagai berikut : oksigen terlarut 6,8 – 7,0 mg/lt, pH 6,0, suhu 21 – 24 O C, karbondioksida 2,2 – 4,5 mg/l, kesadahan 12,3 mg/l, debit air 6,0 – 6,35 liter/detik dan kecerahan lebih dari 2,5 m.
                        Hal utama yang harus diperhatikan dalam budidaya ikan batak adalah kualitas air. Ternyata kualitas yang diinginkan oleh ikan batak berbeda dengan ikan mas, lele dan ikan-ikan lainnya. Ikan mas dan lele mampu hidup di keruh, namun ikan batak tidak, lebih senang hidup di air jernih, yang minim akan pakan alami layaknya kualitas perairan danau toba
                    </p>
                    <p>
                        Cara membedakan jantan dan betina <br>
                        Perbedaann antara jantan dan betina ikan batak dapat dilihat pada tubuhnya. Ikan Jantan memiliki dengan gerakan lincah, bagian belakang sirip dada kasar dan lubang kelamin kemerahan. Apabila diurut ke lubang kelamin keluar cairan putih susu. Sedangkan betina ditandai dengan gerakan lamban, perut lebih lembek dari biasanya.
                        Ikan batak mampu memijah secara alami. Pemijahan dapat dilakukan di kolam tanah yang berair jernih dan berdasar pasir.
                        Berikut adalah beberapa cara pemijahan ikan batak secara alami
                        <ol>
                            <li>Siapkan kolam tanah berukuran 50 m2</li>
                            <li>Keringkan selama 3 – 4 hari</li>
                            <li>Masukan air yang berasal dari mata air atau sumur setinggi 40 – 50 cm dan biarkan mengalir secara terus menerus</li>
                            <li>Masukan 10 ekor induk jantan dan masukan pula 20 ekor induk betina kemudian berikan pakan tambahan berupa pelet dengan dosis 3 persen/hari; biarkan memijah secara alami.</li>
                        </ol> 
                    </p>
                    <p>
                        Pemeliharaan induk <br>
                        Pemeliharaan induk ikan batak dapat dilakukan di kolam tembok.Caranya sebagai berikut:
                        <ol>
                            <li>Siapkan kolam tembok berukuran panjang 5 m, lebar 4 m dengantinggi 1 m</li>
                            <li>Keringkan selama 3 hari kemudian isi dengan air jernih dari mata air atau sumur bor dan biarkan mengalir selama pemeliharaan induk dengan debit minimal 4 liter/detik; masukan 20 ekor induk</li>
                            <li>Berikan pakan berupa pelet dengan dosis 3 persen/hari.</li>
                        </ol>
                    </p>
                    <p>
                        Pemijahan buatan <br>
                        Tanda yang bisa dilihat adalah induk betina nampak tidak tenang. Pada saat itulah induk betina ditangkap, lalu diurut agar telurnya keluar. Telur-telur tersebut ditampung dalam baskom plastik. Bersamaan dengan itu, induk jantan juga ditangkap, lalu diurut agar spermanya keluar. Sperma itu dicampurkan dengan telur. Agar merata, telur dan sperma tersebut diaduk dengan bulu ayam. Bila sudah homogen telur siap dimasukan ke dalam tempat penetasan.
                        Pemijahan buatan dilakukan di bak beton dengan tahapan sebagai berikut:
                        <ol>
                            <li>Siapkan bak beton berukuran panjang 4 m, lebar 2 m dan tinggi 0,8 m</li>
                            <li>Keringkan selama 3 hari, kemudian masukan air jernih setinggi 60 cm dan biarkan mengalir selama pemijahan dengan debit minimal 1 l/detik</li>
                            <li>Pasang hapa halus sesuai dengan ukuran bak, suntik 4 ekor induk betina dengan HCG 500 u/kg dan masukan</li>
                            <li>Setelah 24 jam, suntik kembali induk-induk tersebut dengan ovaprim 0,8 ml/kg dan masukan kembali ke dalam bak pemijahan; suntik induk jantan dengan ovaprim 0,5 ml/kg dan satukan dengan induk-induk betina yang disuntik tadi dan biarkan memijah. </li>
                        </ol>
                    </p>
                    <p>
                        Penetasan dan pemeliharaan larva <br>
                        Penetasan telur ikanbatak dilakukan di akuarium dengan tahapan sebagai berikut:
                        <ol>
                            <li>Siapkan 10 unit akuarium ukuran panjang 80 cm, lebar 60 cm dan tinggi 40 cm</li>
                            <li>Keringkan selama 2 hari kemudian isi air bersih setinggi 30 cm dan pasang tiga buah titik aerasi untuk setiap akuarium dan hidupkan selama penetasan</li>
                            <li>Tebarkan telur secara merata ke permukaan dasar akuarium, setelah  2 – 3 hari kemudian buang sebagian airnya dan tambahkan air baru hingga mencapai ketinggian semula</li>
                            <li>Dua hari kemudian beri pakan berupa Brachionus sp atau Daphnia sp atau Naupli artemia secukupmnya. Lakukan panen pada hari ke 14 dengan menggunakan gayung plastik
                                Larva ini sudah mencapai ukuran 2 – 3 cm dan siap ditebar ke kolam penederan I.</li>
                        </ol>
                    </p>
                    <p>
                        Pendederan <br>
                        Pendederan I ikan batak dilakukan di bak tembok dan plastik. 
                        Caranya adalah sebagai berikut: 
                        <ol>
                            <li> Siapkan bak tembok atau plastik berukuran panjang 3 m, lebar 1 m dan tinggi 0,6 m</li>
                            <li>Keringkan selama 2 hari, kemudian masukan air bersih setinggi 40 cm dan masukan 10.000 larva hasil dari tempat penetasan. Beri pakan berupa tepung pelet</li>
                            <li>Lakukan siphon setiap hari (bersihkan dengan selang) sisa pakan yang tidak termakan. (siphon bila air sudak kotor)</li>
                            <li>Setelah berumur 60 hari, benih-benih sudah mencapai ukuran 5 – 10 cm. Selanjutnya pemeliharaan dilakukan seperti ikan-ikan lainnya.</li>
                        </ol>
